# Case Change
convertVar4 <- toupper("Changing To Upper")
print(convertVar4)

convertVar5 <- tolower("CHANGING TO Lower")
print(convertVar5)

# Substring
subVar <- substring("Hello World", 5, 9)  # 5 to 9
print(subVar)

# Count Char
countVar <- nchar("Count the number of characters")
print(countVar)
